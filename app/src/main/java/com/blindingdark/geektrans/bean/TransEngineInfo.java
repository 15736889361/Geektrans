public class TransEngineInfo {
    String engineName = "";
    String enginePackageName = "";
    String engineSettingsActivityPackageName = "";

    public String getEngineName() {
        return engineName;
    }

    public void setEngineName(String engineName) {
        this.engineName = engineName;
    }

    public String getEnginePackageName() {
        return enginePackageName;
    }

    public void setEnginePackageName(String enginePackageName) {
        this.enginePackageName = enginePackageName;
    }

    public String getEngineSettingsActivityPackageName() {
        return engineSettingsActivityPackageName;
    }

    public void setEngineSettingsActivityPackageName(String engineSettingsActivityPackageName) {
        this.engineSettingsActivityPackageName = engineSettingsActivityPackageName;
    }

}
